# A função print() é uma função embutida (built-in) da linguagem Python.
# Ela é usada para exibir (imprimir) informações no console ou terminal.
# O texto (ou string) que você deseja exibir deve ser colocado entre aspas
# (simples '...' ou duplas "...").
print("Olá Mundo!!!")

# Quando este script é executado, a linha acima fará com que a frase
# "Olá Mundo!!!" apareça na saída padrão (geralmente o console do IDLE
# ou o terminal de onde o script foi chamado).


"""
Exercício 1: Escrevendo, Salvando e Executando seu Primeiro Script Python

Objetivo:
Este exercício prático visa familiarizá-lo com o processo de escrever um
programa Python simples, salvá-lo em um arquivo com a extensão `.py`, e
executá-lo utilizando o ambiente IDLE. Vamos seguir os passos ilustrados
nas Figuras I a V da seção "Atenção" do material do curso [1].

O programa em si será o clássico "Olá Mundo!!!".
"""

# Passo 1: O Código Python
# Este é o código que vamos escrever, salvar e executar.
print("Olá Mundo!!! Este é meu primeiro script Python salvo em um arquivo.")

# --- Instruções para Salvar e Executar usando IDLE (baseado nas Figuras I-V [1]) ---

# 1.  Abra o IDLE:
#     Procure por "IDLE" no menu de aplicativos do seu sistema operacional e abra-o.
#     (Isso corresponde à Figura I e II do material do curso, mostrando a chamada e a tela inicial do IDLE).

# 2.  Crie um Novo Arquivo:
#     No IDLE Shell (a janela que abre inicialmente com o prompt ">>>"), vá no menu
#     `File > New File`. Isso abrirá uma nova janela de editor de texto em branco.

# 3.  Digite o Código:
#     Na nova janela do editor, digite (ou copie e cole) a linha de código Python abaixo:
#     print("Olá Mundo!!! Este é meu primeiro script Python salvo em um arquivo.")

# 4.  Salve o Arquivo:
#     - Vá no menu `File > Save` (ou `File > Save As...`).
#     - Escolha uma pasta em seu computador para salvar seus arquivos do curso.
#     - No campo "File name" (Nome do arquivo), digite um nome para o seu programa,
#       por exemplo: `helloworld.py`
#     - **Importante:** Certifique-se de que o nome do arquivo termina com a extensão `.py`.
#       Esta extensão identifica o arquivo como um script Python [1].
#     - Clique em "Save".

# 5.  Execute o Arquivo:
#     - Com o arquivo `helloworld.py` (ou o nome que você escolheu) ainda aberto na
#       janela do editor do IDLE:
#     - Vá no menu `Run > Run Module`.
#     - Alternativamente, você pode simplesmente pressionar a tecla `F5`.
#     (Isso corresponde à Figura V do material do curso, mostrando o arquivo aberto e
#      a nota sobre como executá-lo).

# 6.  Observe a Saída:
#     - Após executar, a janela do IDLE Shell (a que tem o prompt ">>>") voltará
#       ao foco (ou será reaberta se você a fechou).
#     - Você deverá ver a mensagem:
#       "Olá Mundo!!! Este é meu primeiro script Python salvo em um arquivo."
#       impressa no shell. Isso indica que seu programa foi executado com sucesso!

# Nota sobre as Figuras III e IV do material do curso [1]:
# As Figuras III e IV ilustram como abrir um arquivo `.py` *existente* usando
# `File > Open...`. Você usaria isso se já tivesse salvo o arquivo e quisesse
# abri-lo novamente para editar ou executar.

# Parabéns! Você escreveu, salvou e executou seu primeiro programa Python a partir de um arquivo.
# Esta é uma habilidade fundamental para qualquer desenvolvedor Python.
